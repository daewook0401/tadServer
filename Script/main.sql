CREATE TABLE TB_USER(
    USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_EMAIL VARCHAR2(40) NOT NULL UNIQUE,
    USER_PW VARCHAR2(60) NOT NULL,
    PRIVATE_NAME VARCHAR2(20) NOT NULL,
    USER_NICKNAME VARCHAR2(60) NOT NULL UNIQUE,
    USER_ENROLL_DATE DATE DEFAULT SYSDATE NOT NULL,
    USER_ROLE VARCHAR2(20) DEFAULT 'COMMON' NOT NULL,
    IS_ACTIVE CHAR(1) DEFAULT 'Y' CHECK (IS_ACTIVE IN ('Y', 'N')),
    USER_UPDATE_DATE DATE
);

CREATE TABLE TB_TOKEN(
	TOKEN_NO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	USER_NO NUMBER NOT NULL,
	REFRESH_TOKEN VARCHAR2(255) NOT NULL,
	EXPIRED_DATE TIMESTAMP NOT NULL,
	
	CONSTRAINT FK_TOKEN_USER FOREIGN KEY(USER_NO)
		REFERENCES TB_USER(USER_NO) ON DELETE CASCADE
);

